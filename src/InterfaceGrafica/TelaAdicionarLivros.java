    private void gravaArquivo() throws Exception {
        try (ObjectOutputStream arquivo = new ObjectOutputStream(new FileOutputStream("ArqLivros.bib"))) {
            for (Livro livro1 : livros) {
                arquivo.writeObject(livro1);
            }
        }
    }

