    private void montaCombo() throws FileNotFoundException, IOException, ClassNotFoundException {
        cbEditora.removeAllItems();
        cbEditora.addItem("Selecione uma Editora.");
        ObjectInputStream arquivo = new ObjectInputStream(new FileInputStream("ArqEditora.bib"));
        try {
            do {
                editora = (Editora) arquivo.readObject();
                editoras.add(editora);
            } while (livro != null);
        } catch (EOFException e) {
            arquivo.close();
        }
        Editora editora = null;
        try {
            do {
                editora = (Editora) arquivo.readObject();
                editoras.add(editora);
            } while (editora != null);
        } catch (EOFException e) {
            arquivo.close();
        }

        for (int i = 0; i < editoras.size(); i++) {
            cbEditora.addItem(editoras.get(i).getRazaoSocial());
        }
        cbEditora.setSelectedIndex(0);
    }

